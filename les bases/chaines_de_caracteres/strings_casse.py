# Methode pour changer la casse des carteres
print("> Utilisation de la fonction upper()")
print("Bonjour".upper())
print("> utilisation de la fonction lower()")
print("Bonjour".lower())
print("> Utilisation de la fonction capitalize()")
print("bonjour tous le monde".capitalize())
print("utilisation de la fonction title()")
print("bonjour tous le monde".title())